#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*uc_s;

	uc_s = (unsigned char *)s;
	while (n > 0)
	{
		*uc_s = '\0';
		uc_s++;
		n--;
	}
}
